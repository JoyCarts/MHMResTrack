CREATE TABLE ResMedicalInfo (
   ResID int,
   Illness varchar(1024),
   Allergy varchar(1024),
   DoctorContactID varchar(1024),
   MedicationId varchar(1024),
   MedicalRestrictions varchar(1024),
   DoctorContactID varchar(1024),
   DentistContactID varchar(1024),
   OpticialContactID varchar(1024),
   TherapistContactID varchar(1024),
   CounselorContactID varchar(1024),
   MedicalNoteID varchar(1024),
   LastModifiedDateTime datetime,
   ModifiedBy varchar(10)
);